#include "fdf.h"

t_line init_coords(int x0, int y0, int x1, int y1)
{
	t_line	coords;

	coords.x0 = x0;
	coords.y0 = y0;
	coords.x1 = x1;
	coords.y1 = y1;
	return (coords);
}
